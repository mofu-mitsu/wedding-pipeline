// 16タイプの統一的なキーをマッピングして返す
export const getUnifiedType = (typologySeatStr: string): string => {
  const s = (typologySeatStr || "").toUpperCase().trim();
  if (s.includes("LII") || s.includes("INTP") && s.includes("I") && s.includes("T")) return "LII";
  if (s.includes("ILI") || s.includes("INTJ") && s.includes("I") && s.includes("T")) return "ILI";
  if (s.includes("ILE") || s.includes("ENTP")) return "ILE";
  if (s.includes("LIE") || s.includes("ENTJ")) return "LIE";
  
  if (s.includes("EII") || s.includes("INFP")) return "EII";
  if (s.includes("IEI") || s.includes("INFJ")) return "IEI";
  if (s.includes("IEE") || s.includes("ENFP")) return "IEE";
  if (s.includes("EIE") || s.includes("ENFJ")) return "EIE";
  
  if (s.includes("LSI") || s.includes("ISTJ")) return "LSI";
  if (s.includes("SLI") || s.includes("ISTP")) return "SLI";
  if (s.includes("SLE") || s.includes("ESTP")) return "SLE";
  if (s.includes("SEE") || s.includes("ESFP")) return "SEE";
  
  if (s.includes("ESI") || s.includes("ISFJ")) return "ESI";
  if (s.includes("SEI") || s.includes("ISFP")) return "SEI";
  if (s.includes("ESE") || s.includes("ESFJ")) return "ESE";
  if (s.includes("LSE") || s.includes("ESTJ")) return "LSE";

  // Fallback defaults
  if (s.includes("LII")) return "LII";
  if (s.includes("ILI")) return "ILI";
  if (s.includes("LSI")) return "LSI";
  if (s.includes("SLI")) return "SLI";
  if (s.includes("EII")) return "EII";
  if (s.includes("IEI")) return "IEI";
  if (s.includes("IEE")) return "IEE";
  if (s.includes("EIE")) return "EIE";
  if (s.includes("ILE")) return "ILE";
  if (s.includes("LIE")) return "LIE";
  if (s.includes("SLE")) return "SLE";
  if (s.includes("SEE")) return "SEE";
  if (s.includes("ESI")) return "ESI";
  if (s.includes("SEI")) return "SEI";
  if (s.includes("ESE")) return "ESE";
  if (s.includes("LSE")) return "LSE";

  if (s.includes("INTJ")) return "ILI";
  if (s.includes("INTP")) return "LII";
  if (s.includes("ENTP")) return "ILE";
  if (s.includes("ENTJ")) return "LIE";
  if (s.includes("INFJ")) return "IEI";
  if (s.includes("INFP")) return "EII";
  if (s.includes("ENFP")) return "IEE";
  if (s.includes("ENFJ")) return "EIE";
  if (s.includes("ISTJ")) return "LSI";
  if (s.includes("ISTP")) return "SLI";
  if (s.includes("ESTP")) return "SLE";
  if (s.includes("ESFP")) return "SEE";
  if (s.includes("ISFJ")) return "ESI";
  if (s.includes("ISFP")) return "SEI";
  if (s.includes("ESFJ")) return "ESE";
  if (s.includes("ESTJ")) return "LSE";

  return "LII"; // Fallback default
};

